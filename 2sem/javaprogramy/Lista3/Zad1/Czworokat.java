abstract class Czworokat extends Figura{

    double bok1;
    double bok2;
    double bok3;
    double bok4;
    double kat;
    Czworokat(String nazwa, double bok1, double bok2, double bok3, double bok4, double kat){
        super (nazwa);
        this.bok1=bok1;
        this.bok2=bok2;
        this.bok3=bok3;
        this.bok4=bok4;
        this.kat=kat;
    }
}